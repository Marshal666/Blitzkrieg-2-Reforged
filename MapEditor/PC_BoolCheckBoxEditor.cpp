/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
CPCBoolCheckBoxEditor::CPCBoolCheckBoxEditor() : bDefaultValue( false )
{	
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
CPCBoolCheckBoxEditor::~CPCBoolCheckBoxEditor()
{
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// CPCItemEditor
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool CPCBoolCheckBoxEditor::ActivateEditor( CDialog *pwndActiveDialog )
{
	bDefaultValue = !bDefaultValue;
	if ( GetTargetWindow() )
	{
		SetValueChanged();
		GetTargetWindow()->SendMessage( WM_PC_ITEM_CHANGE, MAKEWPARAM( IC_KILL_FOCUS, PC_TEMPORARY_EDITOR ), 0 );
		return true;
	}
	return false;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void CPCBoolCheckBoxEditor::SetValue( const CVariant &rValue )
{
	bDefaultValue = (bool)rValue;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void CPCBoolCheckBoxEditor::GetValue( CVariant *pValue )
{
	if ( !pValue )
	{
		return;
	}
	//
	*pValue = bDefaultValue;
}
